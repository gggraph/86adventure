
PRINT_BITMAP_256:


;[bp+8]  is x offset pos
;[bp+6]  is y offset pos
;[bp+4]  is bitmap binary pointer

push bp
mov bp, sp

mov edi, [bp+4]

; bmp size is at  [esi]
; bit ptr is at   [esi+10]
; bit count is at [esi+28]

mov eax, 0x18 ; 
cmp al, [edi+28]
jne .end ; this is not a 24 bit depth bitmap


mov eax, 54 ; this is bitarray offset for 24bit bitmap
push eax ; the counter at [bp-4]


; proccess padding. 

mov eax,  dword [edi+18] 
mov ecx, 3
mul ecx
and eax, ecx ; we can also use test 
cmp eax, 0
je .next

.conda:
mov edx, 4
sub edx, eax
push dx
jmp .next
.condb:
xor edx, edx
push dx
.next:

; cx and dx has to be respectively x and y , bl is color 
xor edx, edx
mov dx, [bp+6]
add edx, dword[edi+22] ;; edx is height we can add more 
dec edx

.loopY:
xor eax, eax
mov ax,  [bp+6] ; should clear either low or high then 
cmp edx, eax
jl .end
xor ecx, ecx
mov cx, [bp+8]

	
	.loopX:
	
	xor eax, eax
	mov ax,  [bp+8] ; should clear either low or high then 
	add eax,  dword [edi+18] ; this is an int i guess
	cmp ecx, eax
	je .endX
	;;------------ PRINT THE PIXEL HERE 
	
	; we will do some hack here to convert to grayscale:  

	xor ebx, ebx
	mov esi, dword[bp-4]; the counter
	mov bl,  [edi+esi+1] ; 
	shr bl, 5
	add bl, 10h
	;mov bh, 10h
	add esi, 3
	mov dword[bp-4], esi
	
	push edi
	call PRINT_PIXEL
	pop edi
	
	inc ecx
	jmp .loopX
	

.endX:

; we need to add trailing byte if width is not multiple of 4 .  ( so inc esi by one )
; pad is at [bp-6]
xor esi, esi
add si,  [bp-6];
add esi, dword[bp-4];
mov dword[bp-4], esi

dec edx
jmp .loopY

.end:
add sp, 6
pop bp
ret


