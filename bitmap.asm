;[bp+8]  is x offset pos
;[bp+6]  is y offset pos
;[bp+4]  is bitmap binary pointer

push bp
mov bp, sp

mov edi, [bp+4]

; bmp size is at  [esi]
; bit ptr is at   [esi+10]
; bit count is at [esi+28]

mov eax, 0x18 ; 
cmp al, [edi+28]
jne .end ; this is not a 24 bit depth bitmap

mov eax, 54 ; this is bitarray offset for 24bit bitmap
push eax ; the counter at [bp-4]

; cx and dx has to be respectively x and y , bx is color 

xor edx, edx
mov dx, [bp+6]
add edx, dword[edi+22] ;; edx is height we can add more 
dec edx

.loopY:
xor eax, eax
mov ax,  [bp+6] ; should clear either low or high then 
cmp edx, eax
jl .end
xor ecx, ecx
mov cx, [bp+8]

	
	.loopX:
	
	xor eax, eax
	mov ax,  [bp+8] ; should clear either low or high then 
	add eax,  dword [edi+18] ; this is an int i guess
	cmp ecx, eax
	je .endX
	;;------------ PRINT THE PIXEL HERE 
	
	; safe web palette color = (r*6/256)*36 + (g*6/256)*6 + (b*6/256)
	; we will do some hack here :  

	xor ebx, ebx
	mov esi, dword[bp-4]; the counter
	mov bl,  [edi+esi+1] ; 
	shr bl, 5
	add bl, 10h
	;mov bh, 10h
	add esi, 3
	mov dword[bp-4], esi
	
	push edi
	call PRINT_PIXEL
	pop edi
	
	inc ecx
	jmp .loopX
	

.endX:
dec edx
jmp .loopY

.end:
add sp, 4
pop bp
ret


